f1=fspecial("average",[3,3]);
p4=imfilter(j,f1);
f2=fspecial("average",[3,3]);
p5=imfilter(j,f2);
f3=fspecial("average",[3,3]);
p6=imfilter(j,f3);
figure;
subplot(2,3,1);
imshow(p1);
title('Median Filter (3x3)');
subplot(2,3,2);
imshow(p2);
title('Median Filter (5x5)');
subplot(2,3,3);
imshow(p3);
title('Median Filter (10x10)');

subplot(2,3,4);
imshow(p4);
title('Mean Filter (3x3)');
