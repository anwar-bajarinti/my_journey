clc;
close all;
i=imread("E:\DSP/unnamed.png");
size(i);
k=rgb2gray(i);
size(k);
j=imnoise(k,'salt & pepper',0.06);
p1=medfilt2(j,[3,3]);
p2=medfilt2(j,[5,5]);
p3=medfilt2(j,[10,10]);
